    private void btnCookieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCookieActionPerformed
        cookieCounter = Integer.parseInt(txtPontos.getText());
        txtPontos.setText(Integer.toString(++cookieCounter));
        Random random = new Random();
        ponto = random.nextInt(10)+1;
        if (cookieCounter > 50 && cookieCounter < 99) {
            txtPontos.setText(Integer.toString(ponto+cookieCounter));
        } else if (cookieCounter > 100 && cookieCounter < 999) {
            txtPontos.setText(Integer.toString(15+cookieCounter));
        } else if (cookieCounter > 1000 && cookieCounter < 10000) {
            txtPontos.setText(Integer.toString((ponto*3)+cookieCounter));
            jtxtMensagem.setText("Parabens, voce chegou aos 1000 cookies, ao infinito e alem!");
        } else if (cookieCounter > 10000) {
            txtPontos.setText(Integer.toString((ponto*5)+cookieCounter));
            jtxtMensagem.setText("Pare pessoa, vocÃª vai quebrar o seu mouse ainda!!");
        }
        
    }//GEN-LAST:event_btnCookieActionPerformed

