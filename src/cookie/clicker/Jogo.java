    public void salvamento(){
        try{
            FileWriter escritor = new FileWriter("src/arquivotxt/pontuacao.txt");   
            escritor.write(txtPontos.getText());
            escritor.close();
            JOptionPane.showMessageDialog(rootPane, "Arquivo salvo com sucesso");        
        } catch(Exception e) {
            JOptionPane.showMessageDialog(rootPane, "NÃ£o foi possivel salvar");
        }
    }
    
    public void carregar() throws IOException{
        String arquvio = new String(Files.readAllBytes(Paths.get("src/arquivotxt/pontuacao.txt")));
        txtPontos.setText(arquvio);
    }

